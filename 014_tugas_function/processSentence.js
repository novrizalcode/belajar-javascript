function processSentence (name,age,address,hobby){
    var namaSaya="Nama saya "+name;
    var umurSaya=", Umur saya "+age+" tahun";
    var alamatSaya=", alamat saya di"+address;
    var hobbySaya=", dan hobby saya yaitu "+hobby;
    return namaSaya+umurSaya+alamatSaya+hobbySaya;
}
