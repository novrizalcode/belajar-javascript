function calculateMultiply(num1, num2) {
    return num1 * num2 
}
function pertambahan(num1, num2) {
    return num1 + num2
}
function perkurangan(num1, num2) {
    return num1 - num2
}
function pembagian(num1, num2) {
    return num1 / num2
}
console.log(calculateMultiply(5,6))
console.log(pertambahan(7,5))
console.log(perkurangan(10,5))
console.log(pembagian(12,11))